$(document).ready(function () {
    $(".submitBg").mouseover(function () {
        $(".submitBg").css("background-color", "white");
        $(".submitText").css("color", "white");
    });
    $(".submitBg").mouseout(function () {
        $(".submitBg").css("background-color", "white");
        $(".submitText").css("color", "white");
    });
});